#include <iostream>


int main()
{

    char c = 'a'; // declare a variable of type char and initialize it to the character 'a'
    std::cout << "The value of c is: " << c << '\n';      // print the value of c
    c = 'Z'; // assign the character 'Z' to c
    std::cout << "The new value of c is: " << c << '\n';  // print the new value of c
}